/**
 * Normalize a reg number for comparison
 * Strips spaces, uppercases, normalizes slashes
 */
export function normalizeRegNumber(reg) {
  return reg.trim().toUpperCase().replace(/\s+/g, '');
}

/**
 * Check if a reg number belongs to a final year student
 * Returns the student record if found, null otherwise
 * Primary: exact reg number match (after normalization)
 */
export function checkFinalYear(inputReg) {
  const normalized = normalizeRegNumber(inputReg);
  const student = FINAL_YEAR_STUDENTS.find(
    s => normalizeRegNumber(s.regNumber) === normalized
  );
  return student || null;
}
